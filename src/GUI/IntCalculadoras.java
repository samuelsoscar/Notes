    private void btnCalcularDescActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularDescActionPerformed
        try {
            double montoEntrada = Double.parseDouble(txtMontoDesc.getText());
            double porcentaje =  Double.parseDouble(txtPorcDesc.getText());
            porcentaje = (double)porcentaje/100;
            double total = 0;
            total= montoEntrada*porcentaje;
            total=montoEntrada-total;
            txtResDesc.setText(String.valueOf(total));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Entrada vacía");
        }

    }//GEN-LAST:event_btnCalcularDescActionPerformed

