    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            DecimalFormat df = new DecimalFormat("#,###");
            double total = Double.parseDouble(txtEntradaDolares.getText());
            double impuesto = 0.13;
            double ganancia = Double.parseDouble(txtPorcentajeUtilidad.getText());
            total = total + (total * impuesto);
            total = total + (total * (double)ganancia/100);
            total = total  * Double.parseDouble(txtTipoCambio.getText());
            total = (total * 2)/ 2.0;
            System.out.println(total + "total");
            txtSalidaUtilidad.setText(df.format(total));
          
        }
        catch(NumberFormatException a ){
            JOptionPane.showMessageDialog(this,"Entrada vacía");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

